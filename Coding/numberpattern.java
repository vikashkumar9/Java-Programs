
import java.util.Scanner;
public class numberpattern {
    public static void main (String []args){
        Scanner s=new Scanner(System.in);
        int n=s.nextInt(0);
        int i=1;
        
        while(i<=n){

            int j=1;
        while(j<=i){
System.out.println("1");
j++;
        }
        
        System.out.println();
        i++;
    }
    }
}
